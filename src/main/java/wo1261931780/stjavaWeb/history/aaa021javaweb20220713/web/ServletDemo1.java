package wo1261931780.stjavaWeb.history.aaa021javaweb20220713.web;


import wo1261931780.stjavaWeb.history.aaa021javaweb20220713.pojo.Brand;

import javax.servlet.ServletException;
import javax.servlet.annotation.WebServlet;
import javax.servlet.http.HttpServlet;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;
import java.io.IOException;
import java.util.ArrayList;
import java.util.List;

/**
 * @author liujiajun_junw
 */

@WebServlet("/demo1")
public class ServletDemo1 extends HttpServlet {
	@Override
	protected void doGet(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
		// 1. 准备数据
		List<Brand> brands = new ArrayList<Brand>();
		brands.add(new Brand(1, "三只松鼠", "三只松鼠", 100, "三只松鼠，好吃不上火", 1));
		brands.add(new Brand(2, "优衣库", "优衣库", 200, "优衣库，服适人生", 0));
		brands.add(new Brand(3, "小米", "小米科技有限公司", 1000, "为发烧而生", 1));


		// 2. 存储到request域中
		request.setAttribute("brands", brands);
		request.setAttribute("status", 1);

		// 3. 转发到 el-demo.jsp
		// request.getRequestDispatcher("/el-demo.jsp").forward(request,response);
		// request.getRequestDispatcher("/jstl-if.jsp").forward(request,response);
		request.getRequestDispatcher("/jstl-foreach.jsp").forward(request, response);
	}

	@Override
	protected void doPost(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
		this.doGet(request, response);
	}
}
